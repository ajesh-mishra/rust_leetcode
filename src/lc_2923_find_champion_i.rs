pub struct Solution {}

impl Solution {
    pub fn find_champion(grid: Vec<Vec<i32>>) -> i32 {
        let (mut max_points, mut max_index) = (0, 0);
        for (index, player) in grid.iter().enumerate() {
            let point = player
                .iter()
                .rev()
                .enumerate()
                .map(|(index, p)| (index as i32 + 1) * p)
                .sum();
            if max_points < point {
                max_points = point;
                max_index = index;
            }
        }
        max_index as _
    }
}

